# アルキメデスの角柱(プリズム)
# 上面・下面がk角形であり、側面が正方形であるような角柱を考える
# ３正則グラフなので、この双対グラフとしての三角メッシュを作る関数を作る
my.prism.tri <- function(k){
	top =1
	bottom = k+2
	sides = 2:(k+1)
	sides <- c(sides,2)
	ret <- matrix(0,0,3)
	for(i in 1:k){
		tmp1 <- c(top,sides[i],sides[i+1])
		tmp2 <- c(bottom,sides[i+1],sides[i])
		ret <- rbind(ret,tmp1,tmp2)
	}
	return(ret)
}
my.prism.tri(4)

